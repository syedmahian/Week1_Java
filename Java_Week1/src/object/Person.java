package object;

public class Person {
	
	//Attriubutes of Person class
	
	private String name;
	private int age;
	
	// Constructor for Person Class
	public Person() {
		
		name = "";
		age = 0;
	}
	
	// Overloaded Constructor for Person Class
	
	public Person(String name, int age) {
		
		this.name = name;
		this.age = age;
		
	}

	
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	// Methods for the Person Class
	
	//main method for the Person Class
	

		
		
	}

